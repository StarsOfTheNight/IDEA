package test;

public class Judge {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        if (a%2!=0){
            System.out.println("a是奇数");
        }else{
            System.out.println("a是偶数");
        }
        if (b%2!=0){
            System.out.println("b是奇数");
        }else{
            System.out.println("b是偶数");
        }
    }
}
